// read from usb serial port
int _read(int file, char *data, int len)
{
	while(!usb_char_waiting())
	{
		HAL_Delay(10);
	}

    *data = usb_read_char();
	return 1;
}

// write to usb serial port
int _write(int file, char *ptr, int len)
{
	if(usb_is_configured())
	{
		usb_write_chars(ptr, len);
	}

	return len;
}
